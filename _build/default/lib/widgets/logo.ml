let draw_logo ctx size =
  let padding = size.rows / 32 in
  let lines = String.split_on_char '\n' logo in
  List.iteri (fun i line ->
    LTerm_draw.draw_string ctx i padding (Zed_string.of_utf8 line)
  ) lines

